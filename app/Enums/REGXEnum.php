<?php

namespace App\Enums;

enum REGXEnum: string
{
    case COUNTRY_CODE = "/^\+(?:[0-9]{1,3} ?)$/";
    case MOBILE_NUMBER = '/^[1-9]{1}+[0-9]{7,9}$/';
    case NRC = "/^([0-9]{1,2})\/([A-Z]{5,12})\(([N,P,E,C])\)([0-9]{6})$/";
    case COLOR_CODE = "/(?:#|0x)(?:[a-f,A-F,0-9]{3}|[a-f,A-F,0-9]{6})\b|(?:rgb|hsl)a?\([^\)]*\)/";
    case URL = '/^https?:\\/\\/(?:www\\.)?[-a-zA-Z0-9@:%._\\+~#=]{1,256}\\.[a-zA-Z0-9()]{1,6}\\b(?:[-a-zA-Z0-9()@:%_\\+.~#?&\\/=]*)$/';
    case IPV4 = '/^(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/';
    case UUID = '/^[0-9a-f]{8}-[0-9a-f]{4}-[0-5][0-9a-f]{3}-[089ab][0-9a-f]{3}-[0-9a-f]{12}$/';
    case MAC_ADDRESS = '/^(?:[0-9A-Fa-f]{2}[:-]){5}(?:[0-9A-Fa-f]{2})$/';   //  Mac address regex (IEEE 802)
}
